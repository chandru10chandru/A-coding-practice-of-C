// Write a program to declare and initialize variables of different data types (int, float, char, etc.)
#include <stdio.h>
int main()
{
    int a = 0;
    float b = 15.0;
    char d = 's';
    printf("the value is %d   \n", a);
    printf("the value is %.2f \n ", b); // use %.2f for printing float values
    printf("the value is %c   \n", d);

    return 0;
}